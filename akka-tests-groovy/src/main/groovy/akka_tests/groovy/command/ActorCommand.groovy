package akka_tests.groovy.command

import groovy.transform.EqualsAndHashCode
import groovy.transform.ToString

import akka.actor.ActorRef

/**
 * Command that send the given message to the given actor.
 */
@EqualsAndHashCode
@ToString
class ActorCommand implements Command
{
    // read only properties, with public getter generated by Groovy, but not setter
    final ActorRef actor
    final ActorRef sender
    final Serializable message

    /**
     * Constructor
     *
     * @param actor
     * The Actor Reference, must be not null.
     *
     * @param message
     * The Serializable message to send, must be not null.
	 *
     * @param sender
     * The Actor Reference for the original sender, could be null.
     *
     */
    ActorCommand(ActorRef actor, Serializable message, ActorRef sender)
    {
        assert actor != null, "actor to invoke must be not null."
        assert message != null, "message must be not null."
        // Assert.notNull(sender, "original (sender) actor must be not null.")
        this.actor   = actor
        this.message = message
		this.sender  = sender
    }

    void execute()
    {
        actor.tell(message, sender)
    }

}
